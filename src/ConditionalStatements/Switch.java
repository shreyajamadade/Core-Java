package ConditionalStatements;

public class Switch {
    public static void main(String[] args) {
        int a=1;

        switch(a){
            case 1:
                System.out.println("I am one");
                break;
            case 2:
                System.out.println("I am two");
                break;
            case 3:
                System.out.println("I am three");
                break;
            default:
                System.out.println("Wrong input");
        }
    }
}
